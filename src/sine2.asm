; sine2.asm
; =========================================================  
; Sine demo (full sine wave)
org 0x100

start:
    call init_screen

    call draw_wave

    ; wait for key
    mov ah, 0x00
    int 0x16

    call exit_program


; =========================================================
; Sine routines
; =========================================================

; ----------------------------------------------------------
; draw_wave
; 
; Desroys:
;    AX, BX, CX, DI
draw_wave:
    mov cx, 320
    
    ; BX = x
    xor bx, bx

.draw_loop:
    push cx
    
    ; DI = BX % 256
    mov di, bx

    ; DL = sin_y_table[x % 256]
    mov dl, [sin_y_table + di] 

    ; CL = color
    mov cl, 0x0F

    ; BX = column
    ; DL = row
    ; CL = color
    call put_pixel

    inc bx

    pop cx
    loop .draw_loop 

    ret 

; =========================================================
; Graphics routines
; =========================================================

; ----------------------------------------------------------
; put_pixel
; 
; Put pixel at row and column.
;
; Inputs:
;    ES = screen segmet
;    DL = row, 0..199
;    BX = column, 0..319
;    CL = color
;
; Destroys:
;    AX, DI
put_pixel:
    ; offset = row * 320 + col

    ; row * 320 = row * 256 + row * 64
    ; AX = row
    xor ax, ax
    mov al, dl
    
    ; DI = row
    mov di, ax

    ; AX = row * 256
    shl ax, 8

    ; DI = row * 64
    shl di, 6

    ; DI = row * 320
    add di, ax

    ; DI = row * 320 + column
    add di, bx

    ; write pixel
    mov byte [es:di], cl

    ret

; =========================================================  
; System routines
; =========================================================  


; ----------------------------------------------------------
; init_screen
;
; Set 13h mode (320x200 graphics mode)
; and set ES to the address of the screen buffer. 
; 
; Outputs: 
;     ES = screen segment 
; 
; Destroys:
;     AX
init_screen:
    mov ax, 0x0013
    int 0x10

    mov ax, 0xA000
    mov es, ax
    ret

; ----------------------------------------------------------
; exit_program
;
; Set 03h screen mode.
; 
; Terminate program with 0 return code.
; 
; Destroys:
;     AX
exit_program:
    mov ax, 0x0003
    int 0x10

    mov ax, 0x4C00
    int 0x21



